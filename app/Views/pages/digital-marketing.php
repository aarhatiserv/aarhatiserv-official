<script>
  $('.alert').hide();
  $("#fade1").click(function() {
    var website = $('#webInput').val();
    var email = $('#emailInput').val();
    if (website != "" && email != "") {
      $("#website").text(website);
      $("#userEmail").text(email);

      $('#seo1').fadeOut();
      $('#seo2').fadeIn();
    } else {
      if (website === "") {
        $('.alert').show();
        $('#webInputalert').html("website url is required");
      }
      if (email === "") {
        $('.alert').show();
        $('#emailInputalert').html("website url is required");
      }
    }
  })
  $("#fade2").click(function() {
    $('#seo2').fadeOut();
    $('#seo1').fadeIn();
  })
</script>